#include "dice.hpp"

Dice::Dice(const int sides)
{
    dist = std::uniform_int_distribution<int>(1, sides);
}

int Dice::roll(const int rolls)
{
    int sum = 0;
    for (int i = 0; i < rolls; i++)
    {
        sum += dist(gen);
    }
    return sum;
}